	<p>Unser Kernprojekt ist die Pflege und Wartung unserer ILIAS-Installation. <a href="https://de.wikipedia.org/wiki/ILIAS_%28Software%29">ILIAS</a> ist
	ein freies Lernmanagamentsystem und erfüllt am Fachbereich die Funktion, die universitätsweit durch <a href="http://olat.server.uni-frankfurt.de/">OLAT</a>
	und an anderen Fachbereichen zB durch <a href="http://moodle.studiumdigitale.uni-frankfurt.de">Moodle</a> erbracht wird.

	<p>Unsere eigene Lernplattform zeichnet sich durch kurze Wege aus: Der Kontakt zu Lehrenden ist unmittelbar gegeben. Durch die "Inhouse-Betreuung" können
	wir bei Problemen wenn gewünscht sofort im Büro vorbeikommen, um Hilfestellung zu gewährleisten. Unsere ausgezeichnete Rolle als einzige eLearning-Plattform
	an der Universität, die ausschließlich von Studierenden betrieben wird, gewährt uns Spielraum, um unkonventionell neue Dinge auszuprobieren. So waren wir
	die ersten, die eine <a href="https://elearning.physik.uni-frankfurt.de/local/mobile-werbung/">mobil-Version der Lernplattform</a> angeboten haben oder
	Unterstützung für WebDAV anboten. Auch sind wir ständig dabei, unsere Software technisch attraktiver zu machen. Entstanden ist dabei etwa eine alternative
	ILIAS-Startseite als Erweiterung zur Standardsoftware.

	<p>Ein Lernmanagamentsystem wird von Studierenden oft als etwas empfunden, was ihnen auf dem Weg zu den gewünschten Lernmaterialien im Weg steht. Da wir
	selbst Studierende sind, verstehen wir die Probleme und arbeiten an Lösungen, unsere Software attraktiver zu gestalten. So führen bei uns weniger Klicks
	zum Ziel. Wir erleben im PhysikOnline-Team, dass Lernplattformen niemandem Spaß machen. Deswegen suchen wir nach Alternativen, um den Wünschen von Lehrenden
	gerecht zu werden. Auf diesem Weg ist zum Beispiel das Projekt <a href="pokal.html">POKAL</a> entstanden. Auch die <a href="podcastphysik.html">Podcast-Wiki
	Physik</a> ist ein Produkt unseres Brainstormings, eLearning-Alternativen aufzuzeigen.

	<p>Als studentisches Projekt verstehen wir uns als Sprachrohr und Wegbereiter einer neuen Generation der Studierenden, die in den Medien oft als
	<em>Digital Natives</em> bezeichnet wird. Wir sind stolz darauf, an der Goethe-Universität mit Einrichtungen wie <a href="http://studiumdigitale.uni-frankfurt.de/">StudiumDigitale</a>
	oder dem <a href="http://www.starkerstart.uni-frankfurt.de/">Starken Start</a> zusammenarbeiten zu können. Die exponierte Situation der Goethe Universität
	als Vorreiter des eLearnings in der deutschen Bildungslandschaft sorgt für eine Fülle an Grants, die zur hohen Qualität an neuartigen eLearning-Angeboten
	beitragen. Dort treten wir sowohl selbst als Akteure auf &mdash; etwa als Initiatoren von SeLF-Projekten (Studentischer eLearning-Förderfonds) &mdash; als
	auch beratend für neue Projekte. Dabei verweisen wir oft auf die selbstorganisierte Struktur unseres Projektes. Niemals wegdenken tun wir dabei unsere
	Hauptaufgabe, die ILIAS-Lernplattform zu betreuen.


