	<h1>Podcast-Wiki Physik <small><a href="http://podcast-wiki.physik.uni-frankfurt.de">podcast-wiki.physik.uni-frankfurt.de</a></small></h1>

	<p>Die Podcast-Wiki Physik war das erste separate Projekt von PhysikOnline, nach dem Kernprojekt ILIAS. Es wurde im Rahmen des ersten studentischen
	eLearning-Förderfonds an der Uni Frankfurt im Wintersemester 2008/09 von den PhysikOnline-Mitgründern Marius Schmidt und Jan Uphoff ins Leben gerufen.
	Mit den späteren Mitarbeitern Sarah Ottersbach und Harald Meixner wurde das Dreamteam komplett. Gedreht wurden Arbeitsgruppenvorstellungen, an der
	Tafel vorgerechnete Aufgaben sowie Veranstaltungen (zur damaligen Zeit gab es an der Universität noch keine videoaufzeichnende Hörsaaltechnik) wie die
	Night of Science.

	<p>Das Podcast-Wiki-Projekt wurde durch den Gewinn des hessischen Hochschulpreises für Exzellenz in der Lehre unsterblich in Hinsicht auf eine ständige,
	bis heute anhaltende Förderung durch den Fachbereich. So hat das Projekt einige Generationen von Mitarbeitern erlebt, die sich als Moderatoren, Kameramänner,
	Organisatoren oder Videoschnitttalente verdient gemacht haben.

	<p>Der Begriff <em>Podcast-Wiki</em> ist ein Kofferwort aus einem Podcast und einer Wiki. Während ein Videopodcast etwas weit verbreitetes ist und durch
	Plattformen wie Youtube und Vimeo eine breite Popularität erlangt hat, ist die Verbindung einer Wiki mit Videos neuartig. Den Initiatoren des Projektes
	schwebten dabei eine <a href="https://de.wikipedia.org/wiki/Hypertext">Hypertextualisierung</a> von Informationen in einer kollaborativen Form vor. So sollten
	physikalische Videos und Themen untereinander verlinkt werden. Das studentische Projekt hat eine solche multimedialisierung von Lehrinhalten nie vollständig
	durchdringen können. Ein Beispiel, in welcher Form das geschehen sollte, ist etwa die <a href="http://lp.uni-goettingen.de">Göttinger LP</a>. Während die
	Podcast-Wiki zwar technisch seit einigen Jahren eine echte Wiki (auf Basis von MediaWiki) ist, hat sich an der unvollendeten Umsetzung dieser Idee nichts
	geändert. Dies ist eine der Motivationen, das Projekt in <em>RiedbergTV</em> aufgehen zu lassen.

	<h2>Übergang zu RiedbergTV</h2>

	<p>In den Jahren 2014 und 15 stellte sich langsam heraus, dass das Projekt für größere und regelmäßigere Videoprojekte personell unterbesetzt ist. Auch hat
	sich in der Zeit die (ebenfalls studentisch organisierte) <em>Konkurrenz</em> <a href="http://utv-frankfurt.tumblr.com/">UTV</a> (<em>Unabhängiges Studierendenfernsehen</em>
	an der Uni Frankfurt) etabliert. Zwar liegt der Fokus von UTV unserer Meinung nach eher auf politischen und kulturellen Themen, aber die schiere Größe von
	30 ehrenamtlichen Mitarbeitern zeigt deutlich, dass Videoproduktion <em>Manpower</em> benötigt. Diesem Missstand sind wir durch die Flucht nach vorne begegnet:
	Wir vergrößerten Podcast-Wiki über Fachbereichsgrenzen hinaus auf die gesamten naturwissenschaftlichen Fakultäten der Universität. Damit bedienen wir einerseits
	ein Klientel, dem wir ähnliche Methodik und Vorgehensweisen unterstellen, wie wir es in der Physik gewohnt sind. Andererseits ist eine Positionierung gegenüber
	UTV wohl nicht von der Hand zu weisen. Allerdings lässt sich RiedbergTV nicht ohne weiteres einer politischen Gruppierung zuweisen. Den Naturwissenschaftlern wird
	oft eine unpolitische Haltung vorgeworfen, und ich würde RiedbergTV lieber das Adjektiv "pragmatisch" zuordnen. Allerdings ist Politikverdrossenheit keine
	Eigenschaft, mit der sich Naturwissenschaftler identifizieren. Dem Projekt wird es sicher gut tun, sich sachlichen Themen zu widmen, die studienrelevant sind.
	Das kann aber auch der mangelnde Ausbau des Campus sein.

	<p>RiedbergTV kann auch als <em>Melting Pot</em> von Projekten und studentischen Initiativen verstanden werden, die sich der Medialisierung naturwissenschaftlicher
	Themen verschreiben. So sind Videomaterialien von Podcast-Wiki Physik und <a href="http://biokemika.uni-frankfurt.de">BioKemika</a> in die neue Plattform eingeflossen.
	Auch ist geplant, Deutschlandstipendiaten den Raum und die Mittel zu geben, ihre Vorstellungen von RiedbergTV umzusetzen.

	<p>Wir freuen uns, dass PhysikOnline als Mitinitiator in dem studentischen eLearning-Förderfonds 2015 mit RiedbergTV das höchstdotierte Projekt erlangen hat.
	Wir haben mit diesem Fonds die finanziellen Möglichkeiten bekommen, in vielen Fachbereichen die Ideen der Podcast-Wiki einzubringen.

