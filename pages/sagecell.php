	<h1>SageCell-Server <small><a href="http://sagecell.uni-frankfurt.de>">sagecell.uni-frankfurt.de</a></small></h1>

	<p>Die SageMathCell (SMC) ist eine Website zum Entwickeln, Teilen und Einbetten von kleinen Python- bzw SAGE-Berechnungen. Man kann sich diese Technik als das
	moderne Equivalent von Java-Applets vorstellen, die früher für die multimedialen Lehrbücher der Naturwissenschaften in den 90er und 2000er-Jahren
	entwickelt wurden. Im Gegensatz zu einem Java-Applet, welches die Bewegungsgleichungen für ein Problem der Mechanik etwa numerisch löst, ist der Quellcode in
	einer SageMathCell allerdings in der Programmiersprache Python geschrieben, die &mdash; zusammen mit der Softwareumgebung SAGE &mdash; auf jedem Computer läuft
	und auch in der alltäglichen naturwissenschaftlichen Forschung breite Verwendung findet. Studierende haben hier also die Möglichkeit, anhand von einfachen
	Modellen unmittelbar mit den Techniken konfrontiert zu werden, die sie später brauchen. Im Gegensatz dazu waren die mittlerweile so verhassten Java-Applets
	eine technische Machbarkeitsstudie.

	<p>Im Rahmen der Entwicklung von <a href="pokal.html">POKAL</a> ist uns mit der SMC der kleine Bruder der Computeralgebra-Software <em>SAGE</em> aufgefallen.
	SMC dient dazu, nicht ein gesamtes Sage-Notebook im Web darzustellen, sondern nur eine einzelne Berechnungszelle. Wir decken mit unseren POKAL-Rechenknoten den
	europaweiten Bedarf aller SageMathCell-Rechnungen als Einbindung in ein globales Lastverteilungssystem ab.
