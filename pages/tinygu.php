	<h1>URL-Kürzer für die Uni Frankfurt <small><a href="http://tinygu.de">tinygu.de</a>, <a href="http://goethe.link">goethe.link</a></small></h1>

	<p>Als studentisches Projekt, welches von der personellen und netztechnischen Infrastruktur unseres Institutes, Fachbereiches und unserer
	Universität profitiert, sind wir auch stets betrieben, etwas zurückzugeben. Eine Maßnahme, mit der wir versuchen, den Umgang mit dem Internet
	an der Goethe Universität im Allgemeinen zu verbessern, ist die Etablierung eines Kurz-URL-Dienstes im Sommer 2015.

	<p>Unser Kurz-URL-Dienst ist unter verschiedenen Adressen erreichbar, unter anderem <a href="http://tinygu.de">tinygu.de</a>
	sowie <a href="http://goethe.link">goethe.link</a>. Er zeichnet sich dadurch aus, dass nur Links auf
	<a href="http://tinygu.de/erlaubte-domain-liste">Seiten der Goethe-Universität</a> gekürzt werden.

	<p>Wir sind stolz darauf, als studentische Initiative an der Goethe-Universität den vielerorts geäußerten Wunsch nach sprechenderen
	Webseitenaddressen befriedigen zu können. Die Initiative zeichnet sich durch Offenheit aus: Jeder kann an der Gestaltung des Kurz-URL-Dienstes
	über Github teilhaben. So werden auf <a href="https://github.com/PhysikOnline-FFM/gu-urlshorter/issues">Github: gu-urlshortener</a> Fragen und
	Entscheidungen offen diskutiert, und der komplette <a href="https://github.com/PhysikOnline-FFM/gu-urlshorter">Quellcode des Programmes</a>
	ist online einsehbar.

	<p>Als am Fachbereich Physik etablierte Institution sowie mit <a href="http://tinygu.de/credits">Unterstützung anderer Institute</a> können wir
	die langfristige Verfügbarkeit der kurzen Links gewährleisten. Der Dienst bedarf dennoch stetiger Aufmerksamkeit bei der Anpassung von Policies
	oder der <a href="http://tinygu.de/infos">Entwicklung einer Programmierschnittstelle</a> für Universitätswebsites.


     



