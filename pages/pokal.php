	<p>POKAL steht für <b>P</b>hysik <b>O</b>nlines <b>K</b>ollaborative <b>A</b>rbeits- und <b>L</b>ernplattform. Es handelt sich
	um eine Website, auf der man Rechnungen machen kann und dabei mit Anderen über das Internet die gleichen Inhalte sieht. Diese
	Rechnungen schreibt man in der Sprache <em>Python</em>. Diese Programmiersprache ist im akademischen Umfeld weit verbreitet und
	wird in den einführenden Informatikvorlesungen an der Uni Frankfurt ein Semester lang behandelt.

	<p>Unsere Plattform fußt auf dem Computeralgebra-System <em>SAGE</em>. Dabei handelt es sich um eine Bündlung aller großen
	frei verfügbaren Computeralgebra-Software unter einer einheitlichen Oberfläche. Wir verfolgen damit im eLearning das Paradigma,
	dass wir Benutzern nicht bloß im Umgang mit einer speziellen Software (z.B. dem Marktführer Wolfram Mathematica) schulen möchten,
	sondern ihn zu befähigen versuchen, die Konzepte verschiedener Computeralgebrasysteme zu verstehen und mit vielen verbreiteten
	Tools und symbolischen sowie numerischen Methoden umzugehen.

	<p>In POKAL integriert ist ein Chat, in dem man sich per LaTeX und Datei- sowie Bildaustausch über ein Worksheet unterhalten kann.
	POKAL unterscheidet sich zur Ursprungssoftware SAGE durch einen ausgeprägten Synchronisierungsmodus, sodass man stets die Arbeit
	von Mitarbeitern im aktiven Arbeitsblatt erkennen kann.

	<p>Zielgruppe von POKAL sind Studenten, studentische Arbeitsgruppen sowie der Einsatz in der Lehre (Klassenraum-Szenario). Derzeit
	stellen wir in eigens dafür aufgestellten Rechnern die notwendigen Kapazitäten, um viele gleichzeitige Benutzer bedienen zu können.

